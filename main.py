# -*- coding: utf-8 -*-
# 这是一个简单的 Python 程序，用于打招呼
# This is a simple Python program for greeting
name = "邵锦言"  # 定义一个变量 `name` 来存储你的名字
message = f"你好，{name}！欢迎来到我的 GitHub 项目。期待在IT领域不断学习与成长！" # 使用 f-string 创建一个问候消息

print(message)  # 使用 print() 函数将消息打印到控制台

# 这是一个简单的循环，用于打印数字
# This is a simple loop for printing numbers
print("\n这是一个从1到5的计数：") # 打印提示信息
for i in range(1, 6):  # range(1, 6) 会生成从1到5的数字序列
    print(i)  # 每次循环打印当前数字

# 这是一个简单的数学运算
# This is a simple mathematical operation
num1 = 10
num2 = 5
sum_result = num1 + num2 # 计算两个数字的和
print(f"\n{num1} + {num2} = {sum_result}") # 使用 f-string 打印求和结果

