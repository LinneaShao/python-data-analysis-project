# 这是一个简单的 Python 程序，用于打招呼
name = "邵锦言" # 定义一个变量来存储你的名字
message = f"你好，{name}！欢迎来到我的 GitHub 项目。" # 使用 f-string 创建一个问候消息

print(message) # 打印消息到控制台

# 这是一个简单的循环，打印数字
print("\n这是一个从1到5的计数：")
for i in range(1, 6): # range(1, 6) 会生成从1到5的数字
    print(i) # 打印当前数字

# 这是一个简单的数学运算
num1 = 10
num2 = 5
sum_result = num1 + num2
print(f"\n{num1} + {num2} = {sum_result}") # 打印求和结果